package pnj.remedial.bondan.model;

public class ModelBerita {
    String judul;
    String deskripsi;
    String longDeskripsi;
    String imgBerita;

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }

    public String getLongDeskripsi() {
        return longDeskripsi;
    }

    public void setLongDeskripsi(String longDeskripsi) {
        this.longDeskripsi = longDeskripsi;
    }

    public String getImgBerita() {
        return imgBerita;
    }

    public void setImgBerita(String imgBerita) {
        this.imgBerita = imgBerita;
    }
}

